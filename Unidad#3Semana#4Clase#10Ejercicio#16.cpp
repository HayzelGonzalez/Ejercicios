// En C++ crear un programa que imprima los números del 1 al 5.

#include <iostream>
using namespace std;

int main() 
{
	int i = 1;

	// do...while loop from 1 to 5
	
	do
	{
		cout << i << " ";
		++i;
	}// Fin do.
	
	while (i <= 5);
	
	return 0;
}// Fin int.
